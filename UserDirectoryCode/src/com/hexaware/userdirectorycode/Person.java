package com.hexaware.userdirectorycode;

public class Person {
	
	private String personName;
	private String personAddress;
	private String personTelephoneNo;
	private String personMobileNo;
	private String personHeadOfFamily;
	private String personUniqueIDNo;
	
	public String getPersonName() {
		return personName;
	}
	public void setPersonName(String personName) {
		this.personName = personName;
	}
	public String getPersonAddress() {
		return personAddress;
	}
	public void setPersonAddress(String personAddress) {
		this.personAddress = personAddress;
	}
	public String getPersonTelephoneNo() {
		return personTelephoneNo;
	}
	public void setPersonTelephoneNo(String personTelephoneNo) {
		this.personTelephoneNo = personTelephoneNo;
	}
	public String getPersonMobileNo() {
		return personMobileNo;
	}
	public void setPersonMobileNo(String personMobileNo) {
		this.personMobileNo = personMobileNo;
	}
	public String getPersonHeadOfFamily() {
		return personHeadOfFamily;
	}
	public void setPersonHeadOfFamily(String personHeadOfFamily) {
		this.personHeadOfFamily = personHeadOfFamily;
	}
	public String getPersonUniqueIDNo() {
		return personUniqueIDNo;
	}
	public void setPersonUniqueIDNo(String personUniqueIDNo) {
		this.personUniqueIDNo = personUniqueIDNo;
	}
	
	
	
	//Scanner
}
